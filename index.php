            <p><br>
            <h3> What is the significance of Bodu Beru in Maldivian culture?</h3> <br>
            <p> Bodu Beru, meaning "big drum," is a traditional form of music and dance in the Maldives. It involves drumming, singing, and dancing, and is often performed during celebrations and festivals. Bodu Beru dates back to the 11th century and is believed to have African origins. It is an essential part of Maldivian cultural heritage.
            </p><br>
            <hr>

            <br>
            <h3> How is Maldivian culture influenced by Islam?</h3><br>
            <p>
                Islam is the state religion of the Maldives, and it significantly influences the country's culture, laws, and daily life. Islamic customs and traditions are evident in the celebration of religious festivals such as Ramadan and Eid, the architecture of mosques, and the observance of Islamic dietary laws. Social norms and practices are also shaped by Islamic teachings.</p><br>
            <hr>

            <br>
            <h3> What languages are spoken in the Maldives?</h3><br>
            <p>
                The official language of the Maldives is Dhivehi, which has its own script called Thaana. English is widely spoken and understood, especially in the tourism industry and in business. Arabic is also taught in schools due to the influence of Islam.</p><br>
            <hr>

            <br>
            <h3>What are some major festivals celebrated in the Maldives?</h3>
            <p>Major festivals in the Maldives include Eid al-Fitr and Eid al-Adha, which are Islamic religious holidays celebrated with feasting, prayers, and community gatherings. The Maldives also celebrates Independence Day on July 26th, marking the country's independence from British rule in 1965, and National Day, commemorating the victory of Muhammad Thakurufaanu over the Portuguese in 1573.</p> <br>
            <hr>

            <br>
            <h3> What is the traditional attire of the Maldives?</h3><br>
            <p>
                Traditional Maldivian attire includes the "libaas" for women, a long dress often decorated with gold and silver thread, and the "sarong" or "mundu" for men, a piece of cloth wrapped around the waist, often paired with a white shirt. These outfits are typically worn during special occasions and festivals.</p><br>
